public class JavaOutput {

    public static void main(String[] args) {
        /*
         * println() will add new line for each method
         * double quotes " " are mandatory if worked with text.
         */
        System.out.println("Hello World!");
        System.out.println("I am learning Java.");
        System.out.println("It is awesome!");

        /*
         * print() same as println only not add new line.
         */
        System.out.print("Hello World! ");
        System.out.print("I will print on the same line.");
        System.out.println(" ");

        /*
         * println() and print() also can be used to print number
         * because number != text, we do not use double quotes
         */
        System.out.println(3);
        System.out.println(358);
        System.out.println(3 + 3);
        System.out.println(2 * 5);
    }

}
