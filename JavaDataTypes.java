import java.util.Arrays;

public class JavaDataTypes {
    public static void main(String[] args) {
        /*
         * Variables must have specified data type
         * There are many data types
         * They can divided into two groups
         */

        /*
         * Primitives data type
         * A primitive data type specifies the size and type of variable values, and it
         * has no additional methods.
         */
        int integerDatatype;
        float floatDataType;
        byte byteDataType;
        short shortDataType;
        double doubleDataType;
        boolean booleanDataTpe;
        char charDataType;

        /*
         * Primitive types are predefined (already defined) in Java. Non-primitive types
         * are created by the programmer and is not defined by Java (except for String).
         */
        String stringDataTypes;
        Arrays arrayDataTypes;
    }

}
